    static void waitForReceivedMessages(final List<?> receivedMessages) throws InterruptedException
    {
        for (int attempt = 0; attempt < 100; attempt++)
        {
            if (receivedMessages.size() > 0)
            {
                break;
            }
            Thread.sleep(10);
        }

    }

